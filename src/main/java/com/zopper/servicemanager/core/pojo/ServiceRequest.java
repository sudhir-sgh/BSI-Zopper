package com.zopper.servicemanager.core.pojo;

public class ServiceRequest {
	
	String consumerName;
	String consumerContactNo;
	String consumerShippingAddress;
	String brandServiceJobId;
	String productName;
	String modelName;
	String serialNumber;
	String requestDate;
	String brandId;
	
	public String getBrandId() {
		return brandId;
	}
	public void setBrandId(String brandId) {
		this.brandId = brandId;
	}
	public String getConsumerName() {
		return consumerName;
	}
	public void setConsumerName(String consumerName) {
		this.consumerName = consumerName;
	}
	public String getConsumerContactNo() {
		return consumerContactNo;
	}
	public void setConsumerContactNo(String consumerContactNo) {
		this.consumerContactNo = consumerContactNo;
	}
	public String getConsumerShippingAddress() {
		return consumerShippingAddress;
	}
	public void setConsumerShippingAddress(String consumerShippingAddress) {
		this.consumerShippingAddress = consumerShippingAddress;
	}
	public String getBrandServiceJobId() {
		return brandServiceJobId;
	}
	public void setBrandServiceJobId(String brandServiceJobId) {
		this.brandServiceJobId = brandServiceJobId;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getModelName() {
		return modelName;
	}
	public void setModelName(String modelName) {
		this.modelName = modelName;
	}
	public String getSerialNumber() {
		return serialNumber;
	}
	public void setSerialNumber(String serialNumber) {
		this.serialNumber = serialNumber;
	}
	public String getRequestDate() {
		return requestDate;
	}
	public void setRequestDate(String requestDate) {
		this.requestDate = requestDate;
	}
	

}
